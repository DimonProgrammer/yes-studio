function textBlock(text, style = 'normal', markDefs = [], children = null) {
  return {
    _type: 'block',
    _key: Math.random().toString(36).slice(2, 10),
    style,
    markDefs: markDefs,
    children: children || [{
      _type: 'span',
      _key: Math.random().toString(36).slice(2, 10),
      text,
      marks: [],
    }],
  }
}

function boldSpan(text) {
  return {
    _type: 'span',
    _key: Math.random().toString(36).slice(2, 10),
    text,
    marks: ['strong'],
  }
}

function normalSpan(text) {
  return {
    _type: 'span',
    _key: Math.random().toString(36).slice(2, 10),
    text,
    marks: [],
  }
}

function mixedBlock(children, style = 'normal') {
  return {
    _type: 'block',
    _key: Math.random().toString(36).slice(2, 10),
    style,
    markDefs: [],
    children,
  }
}

